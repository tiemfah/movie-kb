director_of_cast(Cast, Director) :-
    starred(Cast, Movie), 
    directed(Director, Movie).

%----------------------------------------------------------------------------------------------------------------------------

best_rating_movie(Movie) :-
    movieRating(Movie,Rating1), \+
    (
        movieRating(Movie2, Rating2),
        Movie \= Movie2,
        Rating2 > Rating1
    ).

%----------------------------------------------------------------------------------------------------------------------------

best_rating_movie_by_genre(Movie, Genre) :-
    isGenre(Movie, Genre), 
    movieRating(Movie,Rating1),\+
    (
        movieRating(Movie2, Rating2),
        isGenre(Movie2, Genre),
        Movie \= Movie2,
        Rating2 > Rating1
    ).

%----------------------------------------------------------------------------------------------------------------------------

worst_rating_movie(Movie) :-
    movieRating(Movie,Rating1), \+
    (
        movieRating(Movie2, Rating2),
        Movie \= Movie2,
        Rating2 < Rating1
    ).

%----------------------------------------------------------------------------------------------------------------------------

worst_rating_movie_by_genre(Movie, Genre) :-
    isGenre(Movie, Genre), 
    movieRating(Movie,Rating1),\+
    (
        movieRating(Movie2, Rating2),
        isGenre(Movie2, Genre),
        Movie \= Movie2,
        Rating2 < Rating1
    ).

